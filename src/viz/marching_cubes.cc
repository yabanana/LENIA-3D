void MCMesh::clear() {
    vertices.clear();
}

Mesh MCMesh::to_raylib_mesh(bool map_scalar) const {
    Mesh mesh = { 0 };

    int tri_count = static_cast<int>(vertices.size()) / 3;
    if (tri_count == 0) return mesh;

    int vert_count = static_cast<int>(vertices.size());

    mesh.vertexCount  = vert_count;
    mesh.triangleCount = tri_count;

#ifndef RL_MALLOC
    #define RL_MALLOC(sz)      std::malloc(sz)
    #define RL_CALLOC(n, sz)   std::calloc(n, sz)
#endif
    mesh.vertices  = static_cast<float*>(RL_MALLOC(sizeof(float) * 3 * vert_count));
    mesh.normals   = static_cast<float*>(RL_MALLOC(sizeof(float) * 3 * vert_count));
    mesh.texcoords = static_cast<float*>(RL_CALLOC(vert_count, sizeof(float) * 2));
    mesh.colors    = static_cast<unsigned char*>(RL_MALLOC(sizeof(unsigned char) * 4 * vert_count));

    for (int i = 0; i < vert_count; ++i) {
        mesh.vertices[i * 3 + 0] = vertices[i].position.x;
        mesh.vertices[i * 3 + 1] = vertices[i].position.y;
        mesh.vertices[i * 3 + 2] = vertices[i].position.z;

        mesh.normals[i * 3 + 0] = vertices[i].normal.x;
        mesh.normals[i * 3 + 1] = vertices[i].normal.y;
        mesh.normals[i * 3 + 2] = vertices[i].normal.z;

        if (map_scalar) {
            // Map scalar from [-1,+1] to [0,1] for viridis
            float t = (vertices[i].scalar + 1.0f) * 0.5f;
            Color c = colormap(t, ColormapType::VIRIDIS);
            mesh.colors[i * 4 + 0] = c.r;
            mesh.colors[i * 4 + 1] = c.g;
            mesh.colors[i * 4 + 2] = c.b;
            mesh.colors[i * 4 + 3] = 255;
        } else {
            mesh.colors[i * 4 + 0] = 255;
            mesh.colors[i * 4 + 1] = 255;
            mesh.colors[i * 4 + 2] = 255;
            mesh.colors[i * 4 + 3] = 255;
        }
    }

    return mesh;
}

// ===========================================================================
//  Main extraction routine
// ===========================================================================
MCMesh extract_isosurface(const float* data, int N, float threshold, int step,
                          const float* color_field) {
    MCMesh result;
    if (step < 1) step = 1;
    const float s = static_cast<float>(step);

    // Number of z-slabs for OpenMP distribution
    int z_slabs = (N - 1) / step;

    #ifdef _OPENMP
    int num_threads = omp_get_max_threads();
    #else
    int num_threads = 1;
    #endif

    // Thread-local vertex buffers to avoid contention.
    // Pre-reserve an estimate to reduce reallocation.
    std::vector<std::vector<MCVertex>> thread_verts(num_threads);
    {
        std::size_t est_per_thread =
            static_cast<std::size_t>(z_slabs / std::max(1, num_threads))
            * ((N - 1) / step) * ((N - 1) / step) * 3;
        for (auto& v : thread_verts) v.reserve(est_per_thread);
    }

    #pragma omp parallel
    {
        #ifdef _OPENMP
        int tid = omp_get_thread_num();
        #else
        int tid = 0;
        #endif
        auto& local_verts = thread_verts[tid];

        #pragma omp for schedule(dynamic, 4)
        for (int zs = 0; zs < z_slabs; ++zs) {
            int z = zs * step;
            for (int y = 0; y < N - 1; y += step) {
                for (int x = 0; x < N - 1; x += step) {

                    // Evaluate the 8 corner values, gradient normals, and color scalars
                    float corner_val[8];
                    float corner_scalar[8];
                    Vector3 corner_pos[8];
                    Vector3 corner_norm[8];
                    for (int c = 0; c < 8; ++c) {
                        int cx = x + corner_offset[c][0] * step;
                        int cy = y + corner_offset[c][1] * step;
                        int cz = z + corner_offset[c][2] * step;
                        corner_val[c] = sample(data, N, cx, cy, cz);
                        corner_scalar[c] = color_field
                            ? sample(color_field, N, cx, cy, cz)
                            : 0.0f;
                        corner_pos[c] = {
                            static_cast<float>(cx),
                            static_cast<float>(cy),
                            static_cast<float>(cz)
                        };
                        corner_norm[c] = gradient_normal(data, N, cx, cy, cz);
                    }

                    // Build the 8-bit cube index
                    int cube_index = 0;
                    for (int c = 0; c < 8; ++c) {
                        if (corner_val[c] >= threshold) {
                            cube_index |= (1 << c);
                        }
                    }

                    if (edge_table[cube_index] == 0) continue;

                    // Compute interpolated vertices, normals, and scalars on edges
                    Vector3 edge_verts[12];
                    Vector3 edge_norms[12];
                    float   edge_scalars[12];
                    for (int e = 0; e < 12; ++e) {
                        if (edge_table[cube_index] & (1 << e)) {
                            int c0 = edge_connection[e][0];
                            int c1 = edge_connection[e][1];
                            edge_verts[e] = interp_edge(
                                threshold,
                                corner_val[c0], corner_val[c1],
                                corner_pos[c0], corner_pos[c1]);
                            edge_norms[e] = interp_normal(
                                threshold,
                                corner_val[c0], corner_val[c1],
                                corner_norm[c0], corner_norm[c1]);
                            // Interpolate scalar with same parameter
                            float t = 0.5f;
                            if (std::fabs(corner_val[c1] - corner_val[c0]) > 1e-8f) {
                                t = (threshold - corner_val[c0])
                                  / (corner_val[c1] - corner_val[c0]);
                                t = std::max(0.0f, std::min(1.0f, t));
                            }
                            edge_scalars[e] = corner_scalar[c0]
                                + t * (corner_scalar[c1] - corner_scalar[c0]);
                        }
                    }

                    // Emit triangles with smooth normals and per-vertex scalars
                    for (int i = 0; tri_table[cube_index][i] != -1; i += 3) {
                        int e0 = tri_table[cube_index][i + 0];
                        int e1 = tri_table[cube_index][i + 1];
                        int e2 = tri_table[cube_index][i + 2];
                        local_verts.push_back({ edge_verts[e0], edge_norms[e0], edge_scalars[e0] });
                        local_verts.push_back({ edge_verts[e1], edge_norms[e1], edge_scalars[e1] });
                        local_verts.push_back({ edge_verts[e2], edge_norms[e2], edge_scalars[e2] });
                    }
                }
            }
        }
    }

    // Merge thread-local buffers
    std::size_t total = 0;
    for (auto& v : thread_verts) total += v.size();
    result.vertices.reserve(total);
    for (auto& v : thread_verts) {
        result.vertices.insert(result.vertices.end(), v.begin(), v.end());
    }

    return result;
}
